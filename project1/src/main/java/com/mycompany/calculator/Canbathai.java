    private void kqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kqActionPerformed
        // TODO add your handling code here:
         try{
            double number=Double.parseDouble(display.getText());
            double kq=0;
            if(number<=0){
                thongbao.setText("khong the thuc hien phep tinh");
            }
            else{
                kq=Math.sqrt(number);
              display.setText(""+kq);  
            }
            
        }
        catch(NumberFormatException e){
            thongbao.setText("Chua ki tu khong phai la so");
        }
    }//GEN-LAST:event_kqActionPerformed

