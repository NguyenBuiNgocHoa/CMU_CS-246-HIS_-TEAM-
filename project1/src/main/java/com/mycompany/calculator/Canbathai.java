    private void xoatraiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoatraiActionPerformed
        // TODO add your handling code here:
        String manghinh=display.getText(), x="";
        for(int i=1;i<manghinh.length();i++) x+=manghinh.charAt(i);
        display.setText(x);  
    }//GEN-LAST:event_xoatraiActionPerformed

    private void xoaphaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaphaiActionPerformed
        // TODO add your handling code here:
        String manghinh = display.getText();
        StringBuilder builder = new StringBuilder(manghinh); //xay dựng chuỗi
        int length = builder.length(); //lấy chiều dài chuỗi
        if (length > 0) {
        builder.deleteCharAt(length - 1);// xóa phần tử cuối cùng
        }
        String x = builder.toString();
        display.setText(x);
    }//GEN-LAST:event_xoaphaiActionPerformed

